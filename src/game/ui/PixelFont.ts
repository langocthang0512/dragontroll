export class PixelFont {
  constructor(private readonly context: CanvasRenderingContext2D) {}

  measure(text: string, scale: number): number {
    return Math.max(0, text.length * 6 * scale - scale);
  }

  draw(text: string, x: number, y: number, scale: number, color: string, align: CanvasTextAlign = "left", shadow = true): void {
    const normalized = text.toUpperCase();
    const width = this.measure(normalized, scale);
    const startX = align === "center" ? x - width / 2 : align === "right" ? x - width : x;
    if (shadow) this.drawPass(normalized, Math.round(startX + scale), Math.round(y + scale), scale, "#101524");
    this.drawPass(normalized, Math.round(startX), Math.round(y), scale, color);
  }

  private drawPass(text: string, x: number, y: number, scale: number, color: string): void {
    this.context.fillStyle = color;
    for (let index = 0; index < text.length; index++) {
      const glyph = GLYPHS[text[index]] ?? GLYPHS["?"];
      for (let row = 0; row < glyph.length; row++) {
        for (let column = 0; column < glyph[row].length; column++) {
          if (glyph[row][column] === "1") this.context.fillRect(x + index * 6 * scale + column * scale, y + row * scale, scale, scale);
        }
      }
    }
  }
}
